    /*
    * I want to retrieve the user by username
    *
    */


    public function findOneByUsername($value): ?User
    {
        // I take back the QueryBuilder and i sepcify the alias for the entity
        return $this->createQueryBuilder('u')
        // I add the condition for precise i want the username equal to the value set on the parmameter
            ->andWhere('u.username = :val')
        // I precise the parameter
            ->setParameter('val', $value)
        // I transform my object QuerryBuilder on object query 
            ->getQuery()
        // I return the result
            ->getOneOrNullResult()
        ;
    }
    


    public function findOneByEmail($value): ?User
    {
        return $this->createQueryBuilder('u')
            ->andWhere('u.email = :val')
            ->setParameter('val', $value)
            ->getQuery()
            ->getOneOrNullResult()
        ;
    }
}
